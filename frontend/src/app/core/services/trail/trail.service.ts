@Injectable({ providedIn: 'root' })
export class TrailService {
  private readonly http = inject(HttpClient);
  private readonly base = `${environment.apiUrl}/trails`;

  getAllTrails(): Observable<TrailSummaryDto[]> {
    // Swap for real HTTP call when backend is ready:
    // return this.http.get<TrailSummaryDto[]>(this.base);
    return of(MOCK_ALL_TRAILS);
  }

  getTrails(filter: TrailFilterDto): Observable<PageResponseDto<TrailSummaryDto>> {
    const params = new HttpParams({ fromObject: { ...filter } as Record<string, string> });
    return this.http.get<PageResponseDto<TrailSummaryDto>>(this.base, { params });
  }

  getFeaturedTrails(): Observable<TrailSummaryDto[]> {
    // Swap for real HTTP call when backend is ready:
    // return this.http.get<TrailSummaryDto[]>(`${this.base}/featured`);
    return of(MOCK_FEATURED_TRAILS);
  }

  getTrailBySlug(slug: string): Observable<TrailDetailDto> {
    // Swap for real HTTP call when backend is ready:
    // return this.http.get<TrailDetailDto>(`${this.base}/${slug}`);
    return of(MOCK_TRAIL_DETAILS[slug]);
  }

  getMyReviews(): Observable<UserReviewDto[]> {
    return this.http.get<UserReviewDto[]>(`${environment.apiUrl}/users/me/reviews`).pipe(
      catchError(() => of([] as UserReviewDto[])),
    );
  }

  getSimilarTrails(current: TrailDetailDto, limit = 3): Observable<TrailSummaryDto[]> {
    // Swap for real HTTP call when backend is ready:
    // return this.http.get<TrailSummaryDto[]>(`${this.base}/${current.id}/similar`);
    return this.getAllTrails().pipe(
      map(trails =>
        trails
          .filter(t => t.id !== current.id)
          .map(t => {
            let score = 0;
            if (t.region.id === current.region.id)    score += 3;
            if (t.difficulty === current.difficulty)   score += 2;
            if (Math.abs(t.distanceKm - current.distanceKm) <= 30) score += 1;
            return { t, score };
          })
          .sort((a, b) => b.score - a.score || b.t.averageRating - a.t.averageRating)
          .slice(0, limit)
          .map(({ t }) => t),
      ),
    );
  }

  getTrailReviews(slug: string): Observable<ReviewDto[]> {
    return this.http.get<ReviewDto[]>(`${this.base}/${slug}/reviews`);
  }

  submitReview(
    slug: string,
    dto: { rating: number; body: string; authorName: string; authorAvatarInitials: string; trailName: string },
  ): Observable<ReviewDto> {
    return this.http.post<ReviewDto>(
      `${this.base}/${slug}/reviews`,
      { rating: dto.rating, comment: dto.body, trailName: dto.trailName, trailSlug: slug },
    );
  }
}
